//Дано число n (n > 0). Верните из функции значение двойного факториала: n!! = n * (n - 2) * (n - 4) * ... * 2n!!=n∗(n−2)∗(n−4)∗...∗2
//(или 1, в зависимости от четности n).

function testCycle(n) {
    var x=1;
    if (n%2==0){
       for(i=2;i<=n;i=i+2){
           x=x*i
       }
    }
    else{
        for(i=1;i<=n;i=i+2){
            x=x*i
        }
    }

    return x;
}

